    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        // TODO add your handling code here:
        Float VDiskon;
        
        Nama = nama.getText();
        Tlep = tlep.getText();
        Diskon =diskon.getValue();
        if (reguler.isSelected()) {
            jenis= this.reguler.getText();
            VDiskon =Float.valueOf(diskon.getValue());
           Total = 11500*(Float.valueOf(berat.getText()))-((VDiskon/100)*11500);
           uang = Total;
        }
        else if (expres.isSelected()) {
            
           jenis= this.expres.getText();
           VDiskon =Float.valueOf(diskon.getValue());
           Total = 11500*(Float.valueOf(berat.getText()))-((VDiskon/100)*11500);
           uang = (Total/2);
        }
        struk.setText("NAMA \t: "+Nama+
                        "\nNo.Tlep \t: "+Tlep+
                        "\nDISKON \t: "+Diskon+"%"+
                        "\nJASA \t: "+jenis+
                        "\nTOTAL \t:Rp "+uang);
        
        out.setVisible(true);
        in.setVisible(false);
    }//GEN-LAST:event_prosesActionPerformed

