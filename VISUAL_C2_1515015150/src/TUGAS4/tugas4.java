    private void tlepCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tlepCaretUpdate
        // TODO add your handling code here:
        if (tlep.getText().length()==0 && !tlep.getText().equals("A-Z")&& !tlep.getText().equals("a-z")) {
            proses.setEnabled(false);
            
        }
        else if (tlep.getText().length()!=0) {
            proses.setEnabled(true);
        }
    }//GEN-LAST:event_tlepCaretUpdate

    private void beratCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_beratCaretUpdate
        // TODO add your handling code here:
         Integer B;
        B =Integer.valueOf(berat.getText());
        if (B==0) {
            proses.setEnabled(false);
        }
        else if (B!=0) {
            proses.setEnabled(true);
        }
    }//GEN-LAST:event_beratCaretUpdate

