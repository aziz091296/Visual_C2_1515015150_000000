    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
               try{
            int pilih = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapus???", "Hapus", JOptionPane.YES_OPTION);
            if(pilih==JOptionPane.YES_OPTION){
                int baris = jTable1.getSelectedRow();
                model.removeRow(baris);
                
        update.setEnabled(false);
        hapus.setEnabled(false);
        simpan.setEnabled(false);
        
        nama.setText("");
        tlep.setText("");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Klik terlebih dahulu table yang ingin di hapus", "Error!!!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_hapusActionPerformed

