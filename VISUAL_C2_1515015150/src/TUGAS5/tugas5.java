    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
         try{
            int baris = jTable1.getSelectedRow();

            jTable1.setValueAt(nama.getText(), baris, 0);
            jTable1.setValueAt(tlep.getText(), baris, 1);
            jTable1.setValueAt(kota.getSelectedItem(), baris, 2);
            
            nama.requestFocus();
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"Klik terlebih dahulu table yang ingin di update","Error!!!",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_updateMouseClicked

