    void huruf(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"MAAF!!! Kolom ini hanya dapat di isi dengan angka","Error!!!",JOptionPane.WARNING_MESSAGE);
        }
    }
    
    void angka(KeyEvent b){
if(Character.isDigit(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,"MAAF!!! Kolom ini hanya dapat di isi dengan huruf","Error!!!",JOptionPane.WARNING_MESSAGE);
        }    
}
